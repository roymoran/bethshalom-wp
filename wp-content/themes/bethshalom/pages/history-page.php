    <div class="title">THE ISRAELITE ACADEMY</div>
    <div class="history-info">The Israelite Academy traces its origin back to the Ethiopian Hebrew Rabbinical College that was established by Chief Rabbi W. A. Matthew in 1925. As explained in the essay “Who are we?” during the early decades of the twentieth century the term Ethiopian was used in its classical sense to refer to the entire continent of Africa and not just the country of Ethiopia. Therefore, in contemporary terms the title meant black or African American. In 1970, under the leadership of Chief Rabbi Levi Ben Levy, the Ethiopian Hebrew Rabbinical College was renamed the Israelite Rabbinical Academy and established under the charter of the Israelite Board of Rabbis in New York City. For the next thirty years the I.R.A. existed to train and ordain qualified rabbis for service in black communities throughout the United States and the Caribbean. During the summer of 2001, the members of the Israelite Board of Rabbis voted at its International Convention in Chicago to transform the I.R.A. into an institution that would offer courses in Judaic / Israelite education to the general public—not just to aspiring rabbis. This change has allowed for women, laypeople, and those new to the faith to acquire knowledge in a friendly and welcoming environment without the four-year commitment required to enter the rabbinic program and without the prohibitive fees or condescension often found at other institutions.</div>
    <div class="history-info">Thus, for over seventy-five years the Israelite Academy has been a beacon of light to a people struggling to break the chains of darkness. We have brought back more Israelites to the worship of the one, true God of Israel whose ancestors were formerly scattered among the nations of Africa and then slaves in the western hemisphere than any other institution, movement, or organization. We continue our mission today through our affiliated congregations and through our schools. The chart below is a record of our achievement. Following the chart, you will find a list of all the black rabbis—living and deceased—who are recognized by the Israelite Board of Rabbis, Inc.</div>
