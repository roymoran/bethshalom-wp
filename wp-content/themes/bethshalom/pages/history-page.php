    <div class="title">OUR HISTORY</div>
    <div class="history-info">Beth Shalom B’nai Zaken Ethiopian Hebrew Congregation (Beth Shalom) is the oldest Temple in the Chicago area serving the Israelite Community. It was established as the Ethiopian Hebrew Association in 1915, by Rabbi Horace Hasan. In 1918, Rabbi David Lasarus and Rabbi Caino Stirson, came to the Chicago area from New York and later joined forces with Rabbi Hasan. In 1923 our beloved Senior Rabbi Abihu Ben Reuben, (may G-D bless his memory) joined this congregation, which was located at 1850 West Lake Street. In the 1930s and 1940s, the leadership of the Ethiopian Hebrew community was firmly in the hands of Rabbi Abihu Ben Reuben, Rabbi Lazarus, and Rabbi Louis Green. Rabbis Reuben and Lazarus studied with Chief Rabbi Wentworth A. Matthew, at the Commandment Keepers Congregation in New York City, and were ordained rabbis by Chief Rabbi Matthew. Rabbi Louis Green received his Masters of Science in Jewish Studies from the College of Jewish Studies in Chicago, Illinois.</div>
    <div class="history-info">In 1952, Chief Rabbi Wentworth A Matthew came to Chicago to oversee the opening of a new Temple to serve the Ethiopian Hebrew Community. In 1984, the Congregation of Ethiopian Hebrews and Beth Shalom Hebrew Congregation merged to form Beth Shalom Ethiopian Hebrew Congregation. Rabbis Abihu Ben Reuben and Capers C. Funnye, Jr., served the congregation as senior and assistant rabbis. In 1993, Beth Shalom merged with the Congregation of B’nai Zaken, to form Beth Shalom B’nai Zaken Ethiopian Hebrew Congregation, with Rabbi Capers C. Funnye, Jr. as Rabbi.</div>
    <div class="history-info">Beth Shalom is an affiliate congregation of the International Israelite Board of Rabbis Inc. and fully embraces the ideals of Resolution 80lA which was passed by the Board in 1981. It affirms the brotherhood of all people who worship the G-D of Abraham, Isaac and Jacob without regard to tradition or terminology (for example: Black Jews’ Hebrews, Israelites, Jews, etc.) However, when among us, visitors are obliged to respect the customs and traditions followed by members.</div>
    <div class="history-info"><strong>Our Practices and Beliefs</strong></div>
    <div class="history-info">Our particular religious practices were formulated from several sources by our late Honorable Chief Rabbi Wentworth A. Matthew. He was one of the progenitors of our faith, formally called “Black Jews.” Many congregations throughout the United States and the western hemisphere trace their origin to his teachings and to those of his students.</div>
    <div class="history-info">Beth Shalom is a family synagogue. The synagogue exists as a place of worship for all people of the Hebrew-Israelite faith. True to our founding principles, the family unit remains the model upon which all spiritual and secular activities take place. We believe that our way of life promotes strong families and encourages the individuals of those families to be responsible, respected, and productive members of society.</div>
    <div class="history-info">Today, many of us prefer the terms “Hebrew” or “Israelite” for the following reasons: a) These are the terms used in the Torah (Holy Scroll) to refer to the “children of Israel”, b) these terms do not wrongly associate Jewishness with whiteness [which is the prevalent misconception of the term], c) they avoid the changing nomenclatures of terms like Negro, Black, and now African-American.</div>
    <div class="history-info">Although some credible scholars have attempted to determine the racial classification of the ancient Israelites, we believe they were people of African descent. We also believe that G-D is a spirit and those that worship Him “must worship Him in spirit’ instead of pigmentation. There are approximately 5.4 million white Jews in America today and an estimated 150,000 Black Jews or Hebrew Israelites. As a large community, we have theological, philosophical, and political differences that divide us into distinct groups. We here at Beth Shalom are proud of our uniqueness and hope that you will also appreciate and enjoy it.</div>
    <div class="history-info">Our way of life is practiced essentially as Rabbi Matthew and Rabbi Reuben established it. Though the standard classification of “Jews” along Orthodox, Conservative, and Reform lines are an extremely ambiguous set of minhagim (customs), our way of life closely resemble those of the modern orthodox with clear conservative and African-American influences. For instance, a lay person would notice that we maintain separate seating for men and women in our sanctuary, but believe in the complete equality of women. We allow travel on the Shabbat (Sabbath) for worship services, and follow a biblical definition of kosher foods that prohibits the eating of pork and certain kinds of sea food but does not require the separation of milk and meat products. However, we do have members who are strictly kosher according to the Halakah.</div>
    <div class="history-info">We observe all high holy days such as Passover, Shavuot, Rosh Hashana, and Yom Kippur. We recognize holidays such as Hanukkah, Tu-Be-Savat and Purim. We have also instituted days of celebration to honor the mothers, fathers, and children of the congregation. We have established special Shabtot when our young people, woman, and children lead and conduct our worship services. We also have a special Shabbat service when we honor Senior Rabbi Abihu ben Rueben and other leaders of the community.</div>
    <div class="history-info">Beth Shalom has a choir which is led by Sar Nashone Ben Yehudah, an accomplished musician. We have a staff of shamuses (ushers) headed by Rabbi Avraham Ben Isarel. We have classes for beginners and advanced students, as well as Torah study for all members. Beth Shalom is supported by its auxiliaries, Nashe 0r, our Sisterhood and Anshay Chayil, our Brotherhood. We also have a Youth Director who organizes young people in activities that are enjoyable to them. Since life is a learning process, the more we learn the better we should be able to live. This concept is reflected in the schools we operate and in the activities of the temple and auxiliaries. These principles are nurtured in our children, molded and encouraged by our members, and supported by the congregation. </div>
    <div class="history-info">A love of learning should extend from religious to secular topics and from learning about others to learning about ourselves. Membership in Beth Shalom is achieved only after the successful completion of a probationary period, and following the other rules in the congregational by-laws. However, membership is not necessary for regular worship with us; therefore, all are welcome to visit as often as they would like to do so. All rites of the faith (marriages, Bar/Baht Mitzvahs, funerals, counseling letters of reference, etc..) are reserved for members of the congregation exclusively.</div>
